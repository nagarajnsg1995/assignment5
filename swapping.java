
public class swapping {

    public static void main(String[] args) {
        int num1=10;
        int num2=20;
        int result;
        System.out.println("before swaping num1="+num1+ "and num2="+num2 );
       result=num1;
       num1=num2;
       num2=result;
        System.out.println("after swaping num1="+num1+ "and num2="+num2 );
    }
}
